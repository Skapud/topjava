function changeStatus(userId, newStatus) {
    const oldStatus = !newStatus;
    $.ajax({
        type: "PATCH",
        url: ctx.ajaxUrl + userId + "?status=" + newStatus,
    }).done(function () {
        successNoty("User status updated");
    }).fail(function () {
        var $box = $('#checkbox_' + userId);
        $box.prop('checked', oldStatus);
    });
}